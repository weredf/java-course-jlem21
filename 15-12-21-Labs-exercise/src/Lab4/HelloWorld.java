package Lab4;

public class HelloWorld {

	public static void main(String[] args) {
		System.out.println("Hello World");
		System.out.println("Hello World");
		System.out.println("Hello World");
		System.out.println("Hello World");
		System.out.println("Hello World");
		System.out.println("Hello World");
		System.out.println("Hello World");
		System.out.println("Hello World");
		System.out.println("Hello World");
		System.out.println("Hello World");
		System.out.println("==============");

		// incremental for loop
		for (int i = 1; i <= 10; i++) {
			System.out.println("Hello World");
		}
		System.out.println("==============");

		// decremental for loop
		for (int i = 10; i >= 1; i--) {
			System.out.println("Hello World");
		}
		System.out.println("==============");

		// incremental while loop
		int i = 1;
		while (i <= 10) {
			System.out.println("Hello World");
			i++;
		}
		System.out.println("==============");

		// decremental while loop
		i = 10;
		while (i >= 1) {
			System.out.println("Hello World");
			i--;
		}
	}

}
