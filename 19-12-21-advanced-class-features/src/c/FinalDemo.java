package c;

public class FinalDemo {

	public static void main(String[] args) {
		// final on local variable (inside method)
		
		final int x = 12;
	
		// blank final
		final int y;
		System.out.println(1);
		System.out.println(2);
		System.out.println(3);
		y = 5;
		System.out.println(y);
		
		// 1. final on class field (Person.id)
		
		// 2. final on method - cannot be overridden
		
		// 3. final on class - cannot be extended
		
		
	}

}
