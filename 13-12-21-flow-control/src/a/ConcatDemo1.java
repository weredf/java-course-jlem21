package a;

public class ConcatDemo1 {

	public static void main(String[] args) {
		
		String a = "aaa";
		String b = "bbb";
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(a + b);
		System.out.println("===================");
		
		String first = "Wered";
		String last = "Filarski";
		int age = 25;

		System.out.println(first + " " + last + ", " + (age + 2));
		System.out.println("===================");

		int x = 12;
		int y = 36;
		System.out.println(x + " + " + y + " = " + (x + y));

	}

}
